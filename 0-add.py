#!/usr/bin/env python3
"""
Module: add

This module contains a function to add two float numbers.

Functions:
    add(a: float, b: float) -> float:
        Adds two float numbers and returns the result as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns the result.

    Args:
        a (float): The first number to be added.
        b (float): The second number to be added.

    Returns:
        float: The sum of a and b.
    """
    return a + b
