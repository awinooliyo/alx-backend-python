#!/usr/bin/env python3
"""
Module: to_str

This module contains a function to convert
a float number to its string representation.

Functions:
    to_str(n: float) -> str:
        Converts the input float number to
        its string representation and returns it.
"""


def to_str(n: float) -> str:
    """
    Converts a float number to its string representation.

    Args:
        n (float): The float number to convert to string.

    Returns:
        str: The string representation of the input float number.
    """
    return str(n)
